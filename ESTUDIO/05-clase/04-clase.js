let numero2 = Number(prompt('escribe otro numero'));

alert(numero1 + " y " + numero2 + " son iguales: " + (numero1 === numero2));
alert(numero1 + " y " + numero2 + " son distintos: " + (numero1 !== numero2));
alert(numero1 + " es mayor que " + numero2 + ": " + (numero1 > numero2));